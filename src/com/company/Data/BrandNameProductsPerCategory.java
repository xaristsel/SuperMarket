package com.company.Data;

public class BrandNameProductsPerCategory {
	private double freska;
	private double psugiou;
	private double kavas;
	private double peripoiisis;
	private double spitiou;
	private double katoikidiwn;
	public BrandNameProductsPerCategory(double freska, double psugiou, double kavas, double peripoiisis, double spitiou,
			double katoikidiwn) {
		this.freska = freska;
		this.psugiou = psugiou;
		this.kavas = kavas;
		this.peripoiisis = peripoiisis;
		this.spitiou = spitiou;
		this.katoikidiwn = katoikidiwn;
	}
	public double getFreska() {
		return freska;
	}
	public void setFreska(double freska) {
		this.freska = freska;
	}
	public double getPsugiou() {
		return psugiou;
	}
	public void setPsugiou(double psugiou) {
		this.psugiou = psugiou;
	}
	public double getKavas() {
		return kavas;
	}
	public void setKavas(double kavas) {
		this.kavas = kavas;
	}
	public double getPeripoiisis() {
		return peripoiisis;
	}
	public void setPeripoiisis(double peripoiisis) {
		this.peripoiisis = peripoiisis;
	}
	public double getSpitiou() {
		return spitiou;
	}
	public void setSpitiou(double spitiou) {
		this.spitiou = spitiou;
	}
	public double getKatoikidiwn() {
		return katoikidiwn;
	}
	public void setKatoikidiwn(double katoikidiwn) {
		this.katoikidiwn = katoikidiwn;
	}
	
	
	
}
